package edu.uaslp.library.model;

public class Employee
{
    private String name;
    private String clave;

    public void setName(String name) {
        this.name = name;
    }

    public void setClave(String clave) {
        this.clave = clave;
    }

    public String getName() {
        return name;
    }

    public String getClave() {
        return clave;
    }

}
